// Helper function to get governorate by ID
export const getGovernorate = (id) => {
  return syriaLocations.governorates.find(gov => gov.id === id);
};

// Helper function to get city by ID
export const getCity = (id) => {
  for (const gov of syriaLocations.governorates) {
    for (const city of gov.cities) {
      if (city.id === id) return city;
    }
  }
  return null;
};
export const getDistrict = (id) => {
  for (const gov of syriaLocations.governorates) {
    for (const city of gov.cities) {
      for (const district of city.districts) {
        if (district.id === id) return district;
      }
    }
  }
  return null;
};

// Helper function to get location names
export const getLocationName = (id, level, language) => {
  if (level === 'governorate') {
    const gov = getGovernorate(id);
    return gov ? gov.name[language] : id;
  } else if (level === 'city') {
    const city = getCity(id);
    return city ? city.name[language] : id;
  } else if (level === 'district') {
    for (const gov of syriaLocations.governorates) {
      for (const city of gov.cities) {
        for (const district of city.districts) {
          if (district.id === id) return district.name[language];
        }
      }
    }
    return id;
  }
  return id;
};